package com.selenium2.easy.test.server.xml;

/**
 * Web Methods enumeration used to configure a back-end call
 * @author Fabrizio Torelli
 *
 */
public enum WebMethod {
	/**
	 * Requires the GET web method
	 */
	GET, 
	/**
	 * Requires the POST web method
	 */
	PUT, 
	/**
	 * Requires the PUT web method
	 */
	DELETE, 
	/**
	 * Requires the DELETE web method
	 */
	POST
}
