/**
 * Represents the different available kinds of response object for the call
 * @author Fabrizio Torelli
 *
 */
public enum WebResponse {
	/**
	 * Binary response type
	 */
	BINARY, 
	/**
	 * JSON response type
	 */
	JSON, 
	/**
	 * Object response type
	 */
	OBJECT, 
	/**
	 * String response type
	 */
	STRING
}
